def search_products(query, lang="ru"):
    query = query.lower().strip()
    results = []
    for p in PRODUCTS:
        name = p["name_" + lang] if ("name_" + lang) in p else p["name_ru"]
        item = {"name": name, "calories": p["calories"], "protein": p["protein"], "fat": p["fat"], "carbs": p["carbs"]}
        if not query or query in name.lower() or query in p["name_ru"].lower() or query in p.get("name_en", "").lower():
            results.append(item)
    return results

