def classify_product(p):
    name = p["name_ru"].lower()
    for kw in PROTEIN_KEYWORDS:
        if kw in name: return "protein"
    for kw in DAIRY_KEYWORDS:
        if kw in name: return "dairy"
    for kw in CARB_KEYWORDS:
        if kw in name: return "carbs"
    for kw in VEGGIE_KEYWORDS:
        if kw in name: return "veggies"
    for kw in FRUIT_KEYWORDS:
        if kw in name: return "fruits"
    for kw in FAT_KEYWORDS:
        if kw in name: return "fats"
    for kw in DRINK_KEYWORDS:
        if kw in name: return "drinks"
    if p["protein"] > 15: return "protein"
    if p["carbs"] > 20: return "carbs"
    if p["calories"] < 40: return "veggies"
    return "other"

def get_products_by_category():
    cats = {}
    for p in PRODUCTS:
        c = classify_product(p)
        if c not in cats:
            cats[c] = []
        cats[c].append(p)
    return cats

