def generate_meal_plan(plan_type, seed=None):
    """Generate a meal plan by randomly picking products from each category."""
    import random
    if seed is not None:
        rng = random.Random(seed)
    else:
        rng = random.Random()

    cats = get_products_by_category()
    structure = PLAN_STRUCTURES.get(plan_type)
    if not structure:
        structure = PLAN_STRUCTURES["balanced"]

    result = []
    for meal_def in structure["meals"]:
        items = []
        for cat, grams in meal_def["slots"]:
            pool = cats.get(cat, [])
            if not pool:
                pool = cats.get("protein", PRODUCTS[:5])
            product = rng.choice(pool)
            cal_per_100 = product["calories"]
            factor = grams / 100.0
            items.append({
                "name": product["name_ru"],
                "name_en": product.get("name_en", product["name_ru"]),
                "grams": grams,
                "cal": round(cal_per_100 * factor),
                "protein": round(product["protein"] * factor, 1),
                "fat": round(product["fat"] * factor, 1),
                "carbs": round(product["carbs"] * factor, 1)
            })
        result.append({
            "type": meal_def["type"],
            "items": items
        })
    return result
